package br.com.svab.fatec.model;

public class Pais
{
	private Long idPais;
	private String nomePais;
	
	//Getters and Setters
	public Long getIdPais() {
		return idPais;
	}

	public void setIdPais(Long idPais) {
		this.idPais = idPais;
	}

	public String getNomePais() {
		return nomePais;
	}

	public void setNomePais(String nomePais) {
		this.nomePais = nomePais;
	}

}
